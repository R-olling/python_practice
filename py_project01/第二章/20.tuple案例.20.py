"""
    根据如下提供的学生成绩单，完成如下需求：
    1.计算每个学生的总分、各科平均分，然后一并输出出来
    2.统计各科成绩的最低分、最高分、平均分，并输出
    3.查找成绩优秀（平均分大于90）的学生，并输出
"""
"""
学号   | 姓名   | 语文 | 数学 | 英语 
S001  | 王林   | 85   | 92  | 78
S002  | 李慕婉 | 92   | 88   | 95
S003  | 十三   | 78   | 85   | 82
S004  | 曾牛   | 88   | 79   | 91
S005  | 周轶   | 95   | 96   | 89
S006  | 王卓   | 76   | 82   | 77
S007  | 红蝶   | 89   | 91   | 94
S008  | 徐立国 | 75   | 69   | 82
S009  | 许木   | 86   | 89   | 98
S010  | 遁天   | 66   | 59   | 72

"""
# 1. 用元组存储所有学生成绩（最基础的数据格式）
students = (
    ("S001", "王林", 85, 92, 78),
    ("S002", "李慕婉", 92, 88, 95),
    ("S003", "十三", 78, 85, 82),
    ("S004", "曾牛", 88, 79, 91),
    ("S005", "周轶", 95, 96, 89),
    ("S006", "王卓", 76, 82, 77),
    ("S007", "红蝶", 89, 91, 94),
    ("S008", "徐立国", 75, 69, 82),
    ("S009", "许木", 86, 89, 98),
    ("S010", "遁天", 66, 59, 72)
)

print("============== 1. 每个学生的总分、平均分 ==============")
# 循环取出每一个学生的信息
for stu in students:
    sid = stu[0]  # 学号
    name = stu[1]  # 姓名
    yuwen = stu[2]  # 语文
    shuxue = stu[3]  # 数学
    yingyu = stu[4]  # 英语

    total = yuwen + shuxue + yingyu  # 总分
    avg = total / 3  # 平均分

    print(sid, name, "总分：", total, "平均分：", round(avg, 2))

# 打印换行+分隔标题，就是界面排版好看
print("\n============== 2. 各科最低分、最高分、平均分 ==============")

# 定义三个空元组，专门用来存放单科所有分数
yuwen_scores = ()    # 存全部语文成绩
shuxue_scores = ()   # 存全部数学成绩
yingyu_scores = ()   # 存全部英语成绩

# 循环遍历每一个学生元组
for s in students:
    # s[2]是当前学生语文分数，(s[2],)构成单个元素元组
    # += 把分数依次拼进语文成绩元组里
    yuwen_scores += (s[2],)
    # s[3]数学分数，追加存入数学元组
    shuxue_scores += (s[3],)
    # s[4]英语分数，追加存入英语元组
    yingyu_scores += (s[4],)

# 语文统计格式
# round(数值, 保留位数)
# round(x,2) 就是保留两位小数
print("语文：最低", min(yuwen_scores), "最高", max(yuwen_scores), "平均", round(sum(yuwen_scores) / 10, 2))
# 数学统计
print("数学：最低", min(shuxue_scores), "最高", max(shuxue_scores), "平均", round(sum(shuxue_scores) / 10, 2))
# 英语统计
print("英语：最低", min(yingyu_scores), "最高", max(yingyu_scores), "平均", round(sum(yingyu_scores) / 10, 2))

print("\n============== 3. 平均分大于90的优秀学生 ==============")
for stu in students:
    total = stu[2] + stu[3] + stu[4]
    avg = total / 3
    if avg > 90:
        print(stu[0], stu[1], "平均分：", round(avg, 2))